#include "main.h"
/**
 * add - Entry point addition
 * @a: integer
 * @b: integer
 * Return: Always 0 (Success)
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Entry point subtraction
 * @a: integer
 * @b: integer
 * Return: Always 0 (Success)
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Entry point multiplication
 * @a: integer
 * @b: integer
 * Return: Always 0 (Success)
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Entry point division
 * @a: integer
 * @b: integer
 * Return: Always 0 (Success)
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Entry point modulus
 * @a: integer
 * @b: integer
 * Return: Always 0 (Success)
 */
int mod(int a, int b)
{
	return (a % b);
}
